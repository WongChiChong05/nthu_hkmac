pur.addEventListener('click', function(e){
    if (e.target.className === 'box'){
        const show = document.querySelectorAll('.showbox');
        const leave = document.querySelectorAll('.showbox .crossoutBtn');
        const context = document.querySelectorAll('.showbox .purposeText');
        const i = e.target.dataset.id - 1
        show[i].style.top = 0;
        let size = 0;
        if (largeMedia.matches || mediumMedia.matches){
            size = 1;
        }
        else if (smallestMedia.matches){
            size = 2;
        }
        else{
            size = 0;
        }
        console.log(size);
        show[i].style.height = showBoxSize[size];
        if (size === 2){
            show[i].style.overflowY = 'scroll';
        }
        else{
            show[i].style.overflowY = i === leave.length - 1 ? 'scroll' : 'hidden';
        }
        leave[i].style.display = context[i].style.display = 'block';
        leave[i].addEventListener('click', function(){
            show[i].style.height = 0;
            show[i].style.top = showBoxSize[size];
            leave[i].style.display = context[i].style.display = 'none';
        })
        
    }
})

